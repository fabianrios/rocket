<h6 class="bluetxt thin">Cookies</h6>
<p>Una cookie es un pequeño archivo de texto que se almacena en el ordenador de un usuario para propósitos de mantenimiento de registros. Las cookies pueden ser tanto las cookies de sesión o cookies persistentes. Una cookie de sesión expira cuando usted cierra el navegador y se utiliza para hacer más fácil tu navegación en nuestra página web. Una cookie persistente permanece en su disco duro durante un período prolongado de tiempo. Por razones de seguridad, vamos a cifrar cualquier nombre de usuario o contraseñas relacionados con la cuenta de datos que almacenamos en las cookies de este tipo. En el caso de los sitios y servicios que no utilizan un usuario, la cookie contiene un identificador único. Igualmente podemos permitir que nuestros proveedores de servicios autorizados utilicen las cookies de nuestro sitio web para que puedan ayudarnos en las diversas actividades, como hacer el análisis y la investigación sobre la eficacia de nuestro sitio, el contenido y las notificaciones. Tu siempre tienes la posibilidad de eliminar o rechazar las cookies modificando la configuración de tu navegador. (Haga clic en "Ayuda" en la barra de herramientas de la mayoría de los navegadores para obtener instrucciones.) Si lo haces, algunas de las características y servicios de nuestro sitio web pueden  que no funcionen correctamente.</p>
 
<h6 class="bluetxt thin">Web Beacons</h6>
<p>Los web beacons (también conocidas como gifs claros, etiquetas de píxel o web bugs) son pequeños gráficos con un identificador único, similar en función a las cookies, y se utilizan para rastrear los movimientos de los usuarios de la web o para acceder a las cookies. A diferencia de las cookies que se quedarán almacenadas en el disco duro del ordenador del usuario, los web beacons son invisibles incrustados en las páginas web (o en el correo electrónico).  Web pueden ser utilizados para entregar o comunicarse con las cookies, para contar los usuarios que han visitado determinadas páginas y comprender los patrones de uso. También podemos recibir un número de identificación anónima si usted viene a nuestro sitio desde un anuncio en línea que aparece en un sitio web de terceros.</p>					
					

					
